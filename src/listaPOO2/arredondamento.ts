/*Exercício 9
ARREDONDAMENTO – Pesquise, teste e diferencie os métodos de Math: ceil(), floor(),
round() e trunc().*/


let num: number = 4.567;

console.log(`Math.ceil(${num}) = ${Math.ceil(num)}`);   // Arredonda para cima
console.log(`Math.floor(${num}) = ${Math.floor(num)}`); // Arredonda para baixo
console.log(`Math.round(${num}) = ${Math.round(num)}`); // Arredonda para o mais próximo
console.log(`Math.trunc(${num}) = ${Math.trunc(num)}`); // Remove a parte decimal
